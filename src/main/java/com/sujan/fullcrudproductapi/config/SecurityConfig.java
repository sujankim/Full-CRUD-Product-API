package com.sujan.fullcrudproductapi.config;

import com.sujan.fullcrudproductapi.exception.CustomAccessDeniedHandler;
import com.sujan.fullcrudproductapi.exception.CustomAuthenticationEntryPoint;
import org.springframework.security.authentication.AuthenticationManager;
